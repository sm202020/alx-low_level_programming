#include<stdlib.h>

/**
 * add two integers
 * @a: first unt
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
        return (a + b);
}
/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: subtracted final result 
 */
int sub(int a, int b)
{
        return (a - b);
}

/**
 * div - divide two integers
 * @a: first unt
 * @b: second int
 * Return: division result
 */
int Div(int a, int b)
{
        return (a / b);
}
/**
 * mod - mod two integers
 * @a: first unt
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
        return (a % b);
}
